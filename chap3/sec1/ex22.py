# 내장 함수
print("절대값 : ", abs(-1004)) # 1004 출력
print("해당 아스키코드의 문자 : ", chr(65))
print("최대값 : ", max([40, 30, 50, 10, 20]))
print("최소값 : ", min([40, 30, 50, 10, 20]))
print("합계 : ", sum([40, 30, 50, 10, 20]))
print("정렬 : ", sorted([40, 30, 50, 10, 20]))
print("계산식 문자열 : ", eval("5*10-6")) # eval 안에 식 게산하여 출력