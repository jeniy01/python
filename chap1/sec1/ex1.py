# 기본 자료형 - 정수, 실수, 논리, 문자열
a = 1004    # 10진수 정수
b = '김기태'   # 문자열
c = True    # 논리값
g = 3.14    # 실수
print('a=', a)
print('b='+b)
print('c=', c)
print('g=', g)

# 각 진수로 값 대입
d = 0b1101011   # 2진수 대입
e = 0o674   # 8진수 대입
f = 0x3e5   # 16진수 대입
print('d=', d)
print('e=', e)
print('f=', f)

