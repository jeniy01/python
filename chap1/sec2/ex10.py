# 비교연산자와 논리연산자
# 비교연산자 : >, >=, <, <=, ==, !=
# 논리연산자 : and, or, not
a = 30
b = 28
c = 32
print("비교연산자")
print("a>b : ", a>b)    # 크다(초과) - 후
print("a>=b : ", a>=b)  # 크거나 같다(이상) - 이후
print("a<b : ", a<b)    # 작다(미만) - 전
print("a<=b : ", a<=b)  # 작거나 같다(이하) - 이전
print("a==b : ", a==b)  # 같다
print("a!=b : ", a!=b)  # 다르다

print("논리연산자")
