# 사칙연산 : +, -, *, /, %, //, **
a = 60
b = 14
c = "kim"
d = "angel"
print("a+b=", a+b)
print("c+d=", c+d)
print("a-b=", a-b)
# 문자열과 숫자는 더하기 불가
# 문자열은 뺄셈, 나눗셈 불가
print("a*b=", a*b)
print("c*3=", c*3)  # 문자열*숫자는 해당 숫자만큼 반복
print("a/b=", a/b)
print("a//b=", a//b)    #몫 구하기
print("a%b=", a%b)  # 나머지 구하기
print("a**b=", a**b)    # 거듭제곱 : a의 b승